# /usr/bin/env python
# -*- coding=UTF-8 -*-

import turtle
turtle.goto(300,0)
turtle.goto(300,300)
turtle.goto(0,300)
turtle.goto(0,0)
turtle.penup()
turtle.goto(150,0)
turtle.left(90)
turtle.pendown()
turtle.color("green")
turtle.goto(150,300)
turtle.penup()
turtle.goto(300,150)
turtle.right(90)
turtle.pendown()
turtle.goto(0,150)
turtle.forward(150)

turtle.penup()
turtle.goto(0,0)
turtle.pendown()
turtle.color("red")
turtle.right(180)
turtle.forward(300)
turtle.goto(-150,300)
turtle.goto(0,0)
turtle.penup()
turtle.goto(130,21)
turtle.right(135)
turtle.pendown()
turtle.color("blue")
turtle.circle(75)
turtle.done()